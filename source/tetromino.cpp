Block::Block(int type, int rotation) : type(type), rotation(rotation) {}

Block::Block(const Block& block) {
	type = block.type;
	rotation = block.rotation;
	row = block.row;
	col = block.col;
}

int Block::get_type() {
	return type;
}

int Block::get_type(int row_offset, int col_offset) {
	return tetromino[type][rotation][row_offset][col_offset];
}

int Block::get_rotation() {
	return rotation;
}

void Block::set_type(int type) {
	this->type = type;
}

void Block::set_rotation(int rotation) {
	this->rotation = rotation;
}

int Block::get_row() {
	return row;
}

int Block::get_col() {
	return col;
}

void Block::set_row(int row) {
	this->row = row;
}

void Block::set_col(int col) {
	this->col = col;
}

int Block::get_init_row_offset() {
	return tetromino_initial_position[type][rotation][0];
}

int Block::get_init_col_offset() {
	return tetromino_initial_position[type][rotation][1];
}

void Block::swap(Block& a, Block& b) {
	Block temp = a;
	a = b;
	b = temp;
}